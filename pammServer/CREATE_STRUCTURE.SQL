CREATE TABLE IF NOT EXISTS ACCOUNTS (
	ID INTEGER AUTO_INCREMENT UNIQUE, 
	ACCOUNT_NUMBER INTEGER,
	ADMIN_ID VARCHAR(100),
	TRADER_ID INTEGER,
    DATE_REG DATE,
	ID_LAST_OFERT INT, 
	STATE INTEGER,
	START_CAPITAL DECIMAL(10,5),
	CURRENT_CAPITAL DECIMAL(20,5),
	INVESTOR_CAPITAL DECIMAL(20,5),
	AVGRAGE_DROPDOWN DECIMAL(20,5),
	MAXIMAL_DROPDOWN DECIMAL(20,5),
	NUM_OPEN_DEALS INTEGER,
	DATE_NEXT_ROLLOVER DATE
); 

CREATE TABLE IF NOT EXISTS OFERTS (
	ID INTEGER AUTO_INCREMENT UNIQUE, 
	ACCOUNT_ID INTEGER,
	OFERT_ID INTEGER,
	PERIOD INTEGER,
	ADMIN_REWARD DECIMAL(3,2),
	COMMISSION DECIMAL(3,2),
	TYPE INTEGER,
	RESPONSIBILITY FLOAT,
	MIN_SUMM_ADD_AND_LEFT DECIMAL(20,5),	
	STOPOUT_BY_CA DECIMAL(20,5),
	AGENTS_FROM_CALLING DECIMAL(3,2),
	AGENTS_FROM_PROFIT DECIMAL(3,2),
	REPORT_LEVEL VARCHAR(50),
	CONNECTION_METHOD VARCHAR(2048),
	STRATEGY VARCHAR(2048)
);

CREATE TABLE IF NOT EXISTS PROFIT_PER_WEEK(
	ID INTEGER AUTO_INCREMENT UNIQUE, 
	ACCOUNT_NUMBER INTEGER,
	WEEK DATE,
	PROFIT_ON_DOLLAR DECIMAL(20,5),
	PROFIT_ON_PERCENT DECIMAL(20,5),
	PROFIT_OF_INVESTOR DECIMAL(4,4)	
);

CREATE TABLE IF NOT EXISTS PROFIT_PER_WEEK(
	ID INTEGER AUTO_INCREMENT UNIQUE, 
	ACCOUNT_NUMBER INTEGER,
	MOUNTH DATE,
	PROFIT_ON_DOLLAR DECIMAL(20,5),
	PROFIT_ON_PERCENT DECIMAL(20,5),
	PROFIT_OF_INVESTOR DECIMAL(4,4)	
);

CREATE TABLE IF NOT EXISTS DEAL_HISTORY(
	ID INTEGER AUTO_INCREMENT UNIQUE, 
	ACCOUNT_NUMBER INTEGER,
	CLOSED_DEAL_DATE DATE,
	CURRENCY_PAIR VARCHAR(10),
	CAPACITY DECIMAL(20,10),
	GENERAL_PROFIT DECIMAL (20,10),
	ADMIN_CAPITAL DECIMAL (20,10)
);

CREATE TABLE IF NOT EXISTS TRAIDERS(
	ID INTEGER UNIQUE,
	NAME VARCHAR(50),
	SITE VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS FT_ACCOUNT_TYPES(
	ID INTEGER UNIQUE,
	NAME VARCHAR(50)
);

CREATE TABLE IF NOT EXISTS FT_ACCOUNT_STATES(
	ID INTEGER AUTO_INCREMENT UNIQUE, 
	NAME VARCHAR(50)
);	
